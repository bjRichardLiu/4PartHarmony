import java.util.ArrayList;

public class chordList {
    private ArrayList<Chord> chords;

    public chordList(ArrayList<Chord> chords) {
        this.chords = chords;
    }

    public ArrayList<Chord> getChords() {
        return chords;
    }

    public void setChords(ArrayList<Chord> chords) {
        this.chords = chords;
    }
}
